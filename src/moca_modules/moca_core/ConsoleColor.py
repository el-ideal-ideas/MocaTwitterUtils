# -- ConsoleColor --------------------------------------------------------------------------


class ConsoleColor:
    """
    Color data type
    """
    BLACK: str = '\033[30m'
    RED: str = '\033[31m'
    GREEN: str = '\033[32m'
    YELLOW: str = '\033[33m'
    BLUE: str = '\033[34m'
    PURPLE: str = '\033[35m'
    CYAN: str = '\033[36m'
    WHITE: str = '\033[37m'
    END: str = '\033[0m'
    BOLD: str = '\038[1m'
    UNDERLINE: str = '\033[4m'
    INVISIBLE: str = '\033[08m'
    REVERSE: str = '\033[07m'

# -------------------------------------------------------------------------- ConsoleColor --
